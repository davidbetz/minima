using System;
//+
using Themelia.Web.Routing.Data;
//+
namespace Minima.Web.Routing
{
    public class InjectionProcessor : Themelia.Web.Routing.InjectionProcessorBase
    {
        //- @OnAddHttpHandlers -//
        public override void OnAddHttpHandlers(HandlerDataList injectedHandlerList, params Object[] parameterArray)
        {
            SafelyAddHandler(injectedHandlerList, new HandlerData
            {
                Name = "__$Minima$UrlProcessing",
                MatchType = "contains",
                MatchText = "/"
            });
            SafelyAddHandler(injectedHandlerList, new HandlerData
            {
                Name = "__$Minima$BlogDiscovery",
                MatchType = "endswith",
                MatchText = "/rsd.xml"
            });
            SafelyAddHandler(injectedHandlerList, new HandlerData
            {
                Name = "__$Minima$WindowsLiveWriterManifest",
                MatchType = "endswith",
                MatchText = "/wlwmanifest.xml"
            });
            SafelyAddHandler(injectedHandlerList, new HandlerData
            {
                Name = "__$Minima$SiteMap",
                MatchType = "endswith",
                MatchText = "/blogmap.xml"
            });
            SafelyAddHandler(injectedHandlerList, new HandlerData
            {
                Name = "__$Minima$MetaWeblogApi",
                MatchType = "contains",
                MatchText = "/xml-rpc"
            });
            SafelyAddHandler(injectedHandlerList, new HandlerData
            {
                Name = "__$Minima$MetaWeblogApi",
                MatchType = "contains",
                MatchText = "/xml-rpc/"
            });
            SafelyAddHandler(injectedHandlerList, new HandlerData
            {
                Name = "__$Minima$Image",
                MatchType = "contains",
                MatchText = "/imagestore/"
            });
        }
    }
}