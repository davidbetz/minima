using System;
using System.Configuration;
//+
namespace Minima.Configuration
{
    public class AuthenticationElement : Themelia.Configuration.CommentableElement
    {
        //- @DefaultUserName -//
        [ConfigurationProperty("defaultUserName")]
        public String DefaultUserName
        {
            get
            {
                return (String)this["defaultUserName"];
            }
            set
            {
                this["defaultUserName"] = value;
            }
        }

        //- @DefaultPassword -//
        [ConfigurationProperty("defaultPassword")]
        public String DefaultPassword
        {
            get
            {
                return (String)this["defaultPassword"];
            }
            set
            {
                this["defaultPassword"] = value;
            }
        }
    }
}